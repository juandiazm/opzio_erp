<?php
namespace App\traits;

trait string_helper_trait
{
    /*function to get all content between two strings*/
    public function StringHelper_get_string_between($string, $start, $end){
        $string = ' ' . $string;
        $ini = strpos($string, $start);
        if ($ini == 0) return '';
        $ini += strlen($start);
        $len = strpos($string, $end, $ini) - $ini;
        return substr($string, $ini, $len);
    }
}